class CameraManager(object):
    """ Class for camera management"""

    def __init__(self, parent_actor, hud):
        """Constructor method"""
        self.sensor = None
        self.surface = None
        self._parent = parent_actor
        self.hud = hud
        self.recording = False
        self.imageQueue = Queue(maxsize=10) 
        bound_x = 0.5 + self._parent.bounding_box.extent.x
        bound_y = 0.5 + self._parent.bounding_box.extent.y
        bound_z = 0.5 + self._parent.bounding_box.extent.z
        attachment = carla.AttachmentType
        self._camera_transforms = [
            (carla.Transform(carla.Location(x=-2.0*bound_x, y=+0.0*bound_y, z=2.0*bound_z), carla.Rotation(pitch=8.0)), attachment.SpringArm),
            (carla.Transform(carla.Location(x=+0.8*bound_x, y=+0.0*bound_y, z=1.3*bound_z)), attachment.Rigid),
            (carla.Transform(carla.Location(x=+1.9*bound_x, y=+1.0*bound_y, z=1.2*bound_z)), attachment.SpringArm),
            (carla.Transform(carla.Location(x=-2.8*bound_x, y=+0.0*bound_y, z=4.6*bound_z), carla.Rotation(pitch=6.0)), attachment.SpringArm),
            (carla.Transform(carla.Location(x=-1.0, y=-1.0*bound_y, z=0.4*bound_z)), attachment.Rigid)]

        self.transform_index = 1
        self.sensors = [
            ['sensor.camera.rgb', carla.ColorConverter.Raw, 'Camera RGB'],
            ['sensor.camera.depth', carla.ColorConverter.Raw, 'Camera Depth (Raw)'],
            ['sensor.camera.depth', carla.ColorConverter.Depth, 'Camera Depth (Gray Scale)'],
            ['sensor.camera.depth', carla.ColorConverter.LogarithmicDepth, 'Camera Depth (Logarithmic Gray Scale)'],
            ['sensor.camera.semantic_segmentation', carla.ColorConverter.Raw, 'Camera Semantic Segmentation (Raw)'],
            ['sensor.camera.semantic_segmentation', carla.ColorConverter.CityScapesPalette,
             'Camera Semantic Segmentation (CityScapes Palette)'],
            ['sensor.lidar.ray_cast', None, 'Lidar (Ray-Cast)']]
        world = self._parent.get_world()
        bp_library = world.get_blueprint_library()
        for item in self.sensors:
            blp = bp_library.find(item[0])
            if item[0].startswith('sensor.camera'):
                blp.set_attribute('image_size_x', str(hud.dim[0]))
                blp.set_attribute('image_size_y', str(hud.dim[1]))
            elif item[0].startswith('sensor.lidar'):
                blp.set_attribute('range', '50')
            item.append(blp)
        self.index = None

    def toggle_camera(self):
        """Activate a camera"""
        self.transform_index = (self.transform_index + 1) % len(self._camera_transforms)
        self.set_sensor(self.index, notify=False, force_respawn=True)

    def set_sensor(self, index, notify=True, force_respawn=False):
        """Set a sensor"""
        index = index % len(self.sensors)
        needs_respawn = True if self.index is None else (
            force_respawn or (self.sensors[index][0] != self.sensors[self.index][0]))
        if needs_respawn:
            if self.sensor is not None:
                self.sensor.destroy()
                self.surface = None
            self.sensor = self._parent.get_world().spawn_actor(
                self.sensors[index][-1],
                self._camera_transforms[self.transform_index][0],
                attach_to=self._parent,
                attachment_type=self._camera_transforms[self.transform_index][1])

            # We need to pass the lambda a weak reference to
            # self to avoid circular reference.
            weak_self = weakref.ref(self)
            self.sensor.listen(lambda image: CameraManager._parse_image(weak_self, image))
        if notify:
            self.hud.notification(self.sensors[index][2])
        self.index = index

    def next_sensor(self):
        """Get the next sensor"""
        self.set_sensor(self.index + 1)

    def toggle_recording(self):
        """Toggle recording on or off"""
        self.recording = not self.recording
        self.hud.notification('Recording %s' % ('On' if self.recording else 'Off'))

    def render(self, display):
        """Render method"""
        if self.surface is not None:
            display.blit(self.surface, (0, 0))

    @staticmethod
    def _parse_image(weak_self, image):
        self = weak_self()
        if not self:
            return
        if self.sensors[self.index][0].startswith('sensor.lidar'):
            points = np.frombuffer(image.raw_data, dtype=np.dtype('f4'))
            points = np.reshape(points, (int(points.shape[0] / 4), 4))
            lidar_data = np.array(points[:, :2])
            lidar_data *= min(self.hud.dim) / 100.0
            lidar_data += (0.5 * self.hud.dim[0], 0.5 * self.hud.dim[1])
            lidar_data = np.fabs(lidar_data)  # pylint: disable=assignment-from-no-return
            lidar_data = lidar_data.astype(np.int32)
            lidar_data = np.reshape(lidar_data, (-1, 2))
            lidar_img_size = (self.hud.dim[0], self.hud.dim[1], 3)
            lidar_img = np.zeros(lidar_img_size)
            lidar_img[tuple(lidar_data.T)] = (255, 255, 255)
            self.surface = pygame.surfarray.make_surface(lidar_img)
        else:
            image.convert(self.sensors[self.index][1])
            array = np.frombuffer(image.raw_data, dtype=np.dtype("uint8"))
            array = np.reshape(array, (image.height, image.width, 4))
            array = array[:, :, :3]
            array = array[:, :, ::-1]
            self.surface = pygame.surfarray.make_surface(array.swapaxes(0, 1))
            try:
                self.imageQueue.put(image, block=False)
            except Full:
                # Drop oldest image if queue is full
                try:
                    self.imageQueue.get_nowait()
                    self.imageQueue.put(image, block=False)
                except Exception:
                    pass
        if self.recording:
            image.save_to_disk('_out/%08d' % image.frame)

class SpeedLimitDetector(object):
    """Class for detecting speed limit signs and updating the speed limit"""
    
    def __init__(self, world, default_speed_limit=30):
        self.world = world
        self.default_speed_limit = default_speed_limit
        self.current_speed_limit = default_speed_limit
        self.last_sign_location = None
        self.sign_detection_radius = 50  # meters
    
    def update(self):
        """Update the speed limit based on nearby traffic signs"""
        if not self.world.player:
            return self.default_speed_limit
            
        vehicle_location = self.world.player.get_location()
        
        # Get all speed limit signs in the world
        speed_limit_signs = self.world.world.get_actors().filter('traffic.speed_limit.*')
        
        closest_sign = None
        closest_distance = float('inf')
        closest_speed_limit = self.default_speed_limit
        
        for sign in speed_limit_signs:
            # Extract speed limit value from the sign ID (e.g., 'traffic.speed_limit.30')
            sign_id = sign.type_id
            try:
                speed_limit_value = int(sign_id.split('.')[-1])
            except ValueError:
                continue
                
            # Calculate distance to the sign
            distance = vehicle_location.distance(sign.get_location())
            
            # Check if this sign is within detection radius and closer than previously found signs
            if distance < self.sign_detection_radius and distance < closest_distance:
                # Check if we already passed this sign (behind us)
                vehicle_transform = self.world.player.get_transform()
                forward_vector = vehicle_transform.get_forward_vector()
                sign_direction = sign.get_location() - vehicle_location
                
                # Dot product to check if sign is in front of vehicle
                dot_product = forward_vector.x * sign_direction.x + forward_vector.y * sign_direction.y
                
                if dot_product > 0:  # Sign is in front of vehicle
                    closest_sign = sign
                    closest_distance = distance
                    closest_speed_limit = speed_limit_value
        
        # Update current speed limit if we found a valid sign
        if closest_sign:
            self.last_sign_location = closest_sign.get_location()
            self.current_speed_limit = closest_speed_limit
            
        return self.current_speed_limit